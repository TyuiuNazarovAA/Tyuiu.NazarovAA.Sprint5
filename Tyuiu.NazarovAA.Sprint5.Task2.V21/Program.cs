            Console.WriteLine("***************************************************************************");
            Console.WriteLine("* ИСХОДНЫЕ ДАННЫЕ:                                                        *");
            Console.WriteLine("***************************************************************************");

            int[,] mas =
            {
                { 4, 8, 5 },
                { 6, 3, 2 },
                { 9, 4, 5 },
            };
            for (int i = 0; i < mas.GetLength(0); i++)
            {
                for (int j = 0; j < mas.GetLength(1); j++)
                {
                    Console.Write(mas[i,j] + "\t");
                }
                Console.WriteLine();
            }

            Console.WriteLine("***************************************************************************");
            Console.WriteLine("* РЕЗУЛЬТАТ:                                                              *");
            Console.WriteLine("***************************************************************************");

            Console.WriteLine(ds.SaveToFileTextData(mas));
            Console.WriteLine("Файл успешно создан!");

            string res = File.ReadAllText(Path.Combine(Path.GetTempPath(), "OutPutFileTask2.csv"));
            Console.WriteLine(res);
        }
